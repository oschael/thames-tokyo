<script>
    // Initialize service card interactions
    document.addEventListener('DOMContentLoaded', function() {
        const serviceCards = document.querySelectorAll('.service-card');
        
        serviceCards.forEach(card => {
            card.addEventListener('click', function() {
                const details = this.querySelector('.service-details');
                const arrow = this.querySelector('svg');
                const isExpanded = details.classList.contains('expanded');
                
                // Close all other cards
                serviceCards.forEach(otherCard => {
                    if (otherCard !== this) {
                        otherCard.classList.remove('expanded');
                        otherCard.querySelector('.service-details').classList.remove('expanded');
                        otherCard.querySelector('svg').style.transform = 'rotate(0deg)';
                    }
                });
                
                // Toggle current card
                if (isExpanded) {
                    this.classList.remove('expanded');
                    details.classList.remove('expanded');
                    arrow.style.transform = 'rotate(0deg)';
                } else {
                    this.classList.add('expanded');
                    details.classList.add('expanded');
                    arrow.style.transform = 'rotate(180deg)';
                }
            });
        });
        
        // Initialize scroll reveal
        initScrollReveal();
    });
    
    function initScrollReveal() {
        const observerOptions = {
            threshold: 0.1,
            rootMargin: '0px 0px -50px 0px'
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('revealed');
                }
            });
        }, observerOptions);

        document.querySelectorAll('.scroll-reveal').forEach(el => {
            observer.observe(el);
        });
    }
</script>
    
